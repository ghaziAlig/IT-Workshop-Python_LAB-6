#Qustion:   Python program to perform arithmetic operations

a=int(input("Enter first number:  "))
b=int(input("Enter second number:  "))

print("\nThe sum of {0} and {1} is {2}:   \n".format(a,b,a+b))

print("The difference between {0} and {1} is {2}:   \n".format(a,b,a-b))

print("The product of {0} and {1} is {2}:  \n".format(a,b,a*b))

print("The quotient of {0} and {1} is {2}:   \n".format(a,b,a/b))

print("{0} raised to power {1} is {2}:   \n".format(a,b,a**b))
